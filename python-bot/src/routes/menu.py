def get_menu_text() -> str:
    """Форматирует меню в текст для system prompt с калориями"""
    # Группируем по категориям для удобства чтения
    categories = {}
    for name, info in MENU.items():
        cat = info.get("category", "Другое")
        if cat not in categories:
            categories[cat] = []
        categories[cat].append((name, info))

    text = "🍣 МЕНЮ РЕСТОРАНА «САКУРА МАРКЕТ» С КАЛОРИЙНОСТЬЮ:\n\n"

    for cat, items in categories.items():
        text += f"=== {cat.upper()} ===\n"
        for name, info in items:
            text += f"• {name}\n"
            text += f"  Состав: {info['composition']}\n"

            # Выводим вес/объем
            if "weight" in info:
                text += f"  Вес: {info['weight']}\n"
            if "volume" in info:
                text += f"  Объем: {info['volume']}\n"

            # Выводим количество штук, если есть
            if "pieces" in info:
                text += f"  Количество: {info['pieces']} шт.\n"

            # Выводим цену и калории
            text += f"  Цена: {info['price']} ₽\n"

            # Калорийность (разные форматы для разных блюд)
            if "calories_per_piece" in info:
                text += f"  Калорийность: {info['calories_per_piece']} ккал / 1 шт. (порция {info['pieces']} шт. — {info['calories_per_portion']} ккал)\n"
            else:
                text += f"  Калорийность: {info['calories_per_portion']} ккал за порцию\n"

            text += "\n"
    return text

